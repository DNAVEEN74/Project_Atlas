/**
 * Helper function to get icon size styles
 * Use this to apply consistent sizing to Material UI icons
 */
export const getIconSize = (size: 'xs' | 'sm' | 'md' | 'lg' | 'xl' = 'md') => {
    const sizeMap = {
        xs: '1rem',      // 16px
        sm: '1.25rem',   // 20px
        md: '1.5rem',    // 24px
        lg: '2rem',      // 32px
        xl: '2.5rem',    // 40px
    };

    return { fontSize: sizeMap[size] };
};

